package org.zywx.wbpalmstar.plugin.uexqcloudav.vo;

import java.io.Serializable;

/**
 * Created by ylt on 16/3/21.
 */
public class OpenVO implements Serializable {

    private static final long serialVersionUID = 1506920346905370170L;

    private int x;
    private int y;
    private int width;
    private int height;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
